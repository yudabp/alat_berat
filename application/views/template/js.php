    function ubahpass(){
        var old_pass = $("#old_pass").val();
        var new_pass = $("#new_pass").val();
        if(old_pass == "" || new_pass==""){
            swal({
              title: "Data ada yang kosong!",
                text: "Tolong lengkapi data.",
                type: "warning",
                icon: 'warning',
                buttonsStyling: false,
                confirmButtonClass: "btn btn-warning"
            }).catch(swal.noop);
        }else{
          $.ajax({
              url : "<?php echo base_url(); ?>getPass",
            type: "POST",
            dataType: "JSON",
            data: {
                old_pass :old_pass,
                new_pass :new_pass,
            },
            success : function(data){
                if(data == 0){
                  swal({
                        title: 'Gagal!',
                        text: 'Password tidak ditemukan',
                        type: 'error',
                        confirmButtonClass: "btn btn-danger",
                        buttonsStyling: false
                    }).catch(swal.noop)
                }else{
                  $.ajax({
                    url : "<?php echo base_url(); ?>uptPass",
                          type: "POST",
                          dataType: "JSON",
                          data: {
                            old_pass :old_pass,
                            new_pass :new_pass,
                          }
                  });
                  $("#formpass").modal("hide");
                    swal({
                        title: "Congratulation!",
                        text: "Password has been changed. Please login with new password",
                        type: "success",
                        icon: 'success',
                        buttonsStyling: false,
                        confirmButtonClass: "btn btn-success"
                    },function(){
                      window.location.href="<?php echo base_url(); ?>proout";
                    });
                }
            },
            error : function(jqXHR, textStatus, errorThrown){
              swal({
                    title: 'Gagal!',
                    text: 'Gagal mengambil data.',
                    type: 'error',
                    confirmButtonClass: "btn btn-danger",
                    buttonsStyling: false
                }).catch(swal.noop)
            }
          });
        }
    }

